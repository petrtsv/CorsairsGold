package site.petrtsv.corsairs.screens;

import com.badlogic.gdx.Gdx;
import com.badlogic.gdx.graphics.GL20;

import site.petrtsv.corsairs.Corsairs;
import site.petrtsv.corsairs.controllers.GameController;
import site.petrtsv.corsairs.models.GameWorld;

/**
 * Created by Петр on 04.07.2017.
 * <p>
 * Screen with the game.
 */
public class GameScreen extends AppScreen
{
	private static final float MAX_FPS = 120;
	@SuppressWarnings("CanBeFinal")
	private int width;
	@SuppressWarnings("CanBeFinal")
	private int height;
	private float time;

	@SuppressWarnings("CanBeFinal")
	private Corsairs game;
	@SuppressWarnings("CanBeFinal")
	private GameController controller;
	@SuppressWarnings("CanBeFinal")
	private GameWorld model;


	public GameScreen(Corsairs game, int width, int height)
	{
		this.game = game;
		this.width = width;
		this.height = height;

		model = new GameWorld(this);
		controller = new GameController(model);


	}

	@Override
	public void show()
	{
		Gdx.input.setInputProcessor(controller);
	}

	@Override
	public void render(float delta)
	{
		time += delta;
		if (time >= 1 / MAX_FPS)
		{

			Gdx.gl.glClearColor(82.0f / 255.0f, 219.0f / 255.0f, 255.0f / 255.0f, 1);
			Gdx.gl.glClear(GL20.GL_COLOR_BUFFER_BIT);
			model.act(delta);
			model.draw();
		}

	}

	@Override
	public void resize(int width, int height)
	{

	}

	@Override
	public void pause()
	{
		model.setState(GameWorld.WorldState.PAUSED);
	}

	@Override
	public void resume()
	{

	}

	@Override
	public void hide()
	{

	}

	@Override
	public void dispose()
	{
		model.dispose();
	}

	@Override
	public Corsairs getGame()
	{
		return game;
	}

	@Override
	public int getWidth()
	{
		return width;
	}

	@Override
	public int getHeight()
	{
		return height;
	}
}
























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































